var x = 5, result;

// 선할당 후증가(postfix increment operator)
result = x++;
console.log(result, x);

// 선증가 후할당(prefix increment oprator)
result = ++x;
console.log(result, x);

// 선할당 후감소(postfix decrement oprator)
result = x--;
console.log(result, x);

// 선감소 후할당(prefix decrement oprator)
result = --x;
console.log(result, x);